export function add(a, b) {
    return a + b;
}

export function subtract(a, b) {
    return a - b;
}

export function multiply(a, b) {
    return a * b;
}

export function divide(a, b) {
    return a / b;
}

export function mod(a, b) {
    return a % b;
}

export function pow(a, b) {
    return a ** b;
}